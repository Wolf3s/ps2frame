//************************************************************
//                                                           *
//   Name    :	SaveDIB                                      *
//   Function:  Save a DIB section inside a .BMP             *
//                                                           *
//   In   :	FileName = name to save as						 *
//          Width  = width of image                          *
//          Height = height of image                         *
//   Out  : none                                             *
//															 *
// Notes:													 *
//   GetPaletteEntry(i), returns THAT palette index			 *
//	 Watch for Red and Blue swapping						 *
//															 *
//	Author			Version		Comments					 *
//  ------													 *
//	Bigboy			1.0			First version 256 colour only*
//                                                           *
//************************************************************
//
// must PACK struct, this is usually done using 1 ofthe following PACK methods
//
//#pragma pack(push,1)
//#pragma pack(1)
//__attribute__((packed)) after each item to pack
//
typedef	struct tDIB 
{
	//
	//File header
	//
	u16		ASCII_BM			__attribute__((packed));			//"BM" ID string
	u32		filesize			__attribute__((packed));
	u16		reserved1			__attribute__((packed));
	u16		reserved2			__attribute__((packed));
	u32		ImageOffset			__attribute__((packed));
	//
	// Image header
	//
	u32		SizeofImageHeader	__attribute__((packed));			// always 40 bytes
	u32		ImageWidth			__attribute__((packed));			// in pixels
	u32		ImageHeight			__attribute__((packed));			// in pixels
	u16		Planes				__attribute__((packed));			// always 1
	u16		BitsPerPixel		__attribute__((packed));			// (1,4,8,24)
	u32		Compression			__attribute__((packed));			// 0=none, other=RLE
	u32		CompressedImageSize	__attribute__((packed));			// in bytes
	u32		HorizRes			__attribute__((packed));			// in pixels per meter?!?!?
	u32		VertRes				__attribute__((packed));			// in pixels per meter?!?!?
	u32		NumberCols			__attribute__((packed));			// palette entries used.
	u32		NumberImport		__attribute__((packed));			// important colours in palette?
	u8		Palette[256*4]		__attribute__((packed));			// palette 4*N (B,G,R)
}tDIB __attribute__((packed));
//#pragma pack()
//#pragma pack(pop)


void SaveDIB( char *FileName, int Width, int Height )
{
	int		handle;
	s32		i,i2;
	tDIB	DIB;
	u8		*pScr;
	u32		c;
	char	name[1024];

//	printf("Sizeof DIB:  %d\n",sizeof(tDIB));		// should be 1078 bytes - 54+(256*4)

	//
	// File Header
	//
	DIB.ASCII_BM=0x4d42;										//"BM"
	DIB.filesize=sizeof(tDIB)+(Width*Height);					// file size
	DIB.reserved1=0;
	DIB.reserved2=0;
	DIB.ImageOffset=sizeof(tDIB);								// start of image
	//
	// Image header
	//
	DIB.SizeofImageHeader=40;									// always 40
	DIB.ImageWidth=Width;										// in pixels
	DIB.ImageHeight=Height;										// in pixels
	DIB.Planes=1;												// always 1
	DIB.BitsPerPixel=8;											// (1,4,8,24)
	DIB.Compression=0;											// 0=none, other=RLE
	DIB.CompressedImageSize=0;									// in bytes
	DIB.HorizRes=0;												// in pixels per meter?!?!?
	DIB.VertRes=0;												// in pixels per meter?!?!?
	DIB.NumberCols=256;											// palette entries used.
	DIB.NumberImport=256;										// important colours in palette?

	i2=0;
	for (i=0;i<256;i++){
		U32	ABGR;
		ABGR = GetPaletteEntry(i);
		DIB.Palette[i2  ]=(ABGR>>16)&0xff;						// B
		DIB.Palette[i2+1]=(ABGR>>8)&0xff;						// G
		DIB.Palette[i2+2]=(ABGR)&0xff;							// R
		DIB.Palette[i2+3]=0;
		i2+=4;
	}

	sprintf(&name[0],"host:\\%s",FileName);						// HOST only, cant save to CDROM0!!!
	handle = fio_open( &name[0], O_RDWR|O_CREAT|O_TRUNC ); 		// system protection, read only, write protected etc... where?!?!?!
	fio_write( handle, &DIB, sizeof(tDIB));						// write header

	// Now, write image out upside down...
	for (i=(Height-1);i>=0;i--)
	{
		c=(i*Width);
		pScr=pScreen;
		pScr+=c;
		fio_write( handle,pScr, Width);							// writes out a line at a time... slower... but less memory
	}
	fio_close( handle );
}
